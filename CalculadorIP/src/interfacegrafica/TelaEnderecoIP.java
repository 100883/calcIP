        // TODO add your handling code here:

        EnderecoIP teste = new EnderecoIP();

        int OctetoIp1 = Integer.parseInt(jTextFieldPrimeiraSentenca.getText());
        teste.setOctetoIp1(OctetoIp1);

        int OctetoIp2 = Integer.parseInt(jTextFieldSegundaSentenca.getText());
        teste.setOctetoIp2(OctetoIp2);

        int OctetoIp3 = Integer.parseInt(jTextFieldTerceiraSentenca.getText());
        teste.setOctetoIp3(OctetoIp3);

        int OctetoIp4 = Integer.parseInt(jTextFieldQuartaSentenca.getText());
        teste.setOctetoIp4(OctetoIp4);

        int BytsUser = Integer.parseInt(jTextFieldBytsUser.getText());
        teste.setBytsUser(BytsUser);

        String saida6 = teste.CalcularBtysMascaraDecimal();
        String saida7 = teste.CalcularBtysMascaraBinario();
        String saida8 = teste.qtdHost();
        String saida9 = teste.CalcularIPBrodcast();
        String saida10 = teste.CalcularUltimoRede();
        String saida11 = teste.CalcularPrimeiroRede();
        String saida12 = teste.CalcularIPRede();
        String saida13 = teste.qtdHost1();

        jTextPaneMascaraDecimal1.setText(saida6);
        jTextPaneMascaraBinaria1.setText(saida7);
        jTextPaneTotIP.setText(saida12);
        jTextPaneIpBrodcast1.setText(saida9);
        jTextPanePrimIPV1.setText(saida11);
        jTextPaneUltIPV1.setText(saida10);
        jTextPaneTotIP.setText(saida8);
        jTextPaneTotIpUso.setText(saida13);
        jTextPaneIpRede1.setText(saida12);
